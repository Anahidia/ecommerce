import { Injectable } from '@nestjs/common';
import { PassportStrategy } from '@nestjs/passport';
import { Strategy, VerifyCallback } from 'passport-google-oauth20';

@Injectable()
export class GoogleStrategy extends PassportStrategy(Strategy, 'google') {
  constructor() {
    super({
      clientID: process.env.GOOGLE_CLIENT_ID, // Configura tu CLIENT_ID en el archivo .env
      clientSecret: process.env.GOOGLE_CLIENT_SECRET, // Configura tu CLIENT_SECRET en el archivo .env
      callbackURL: 'http://localhost:3000/auth/google/callback', // Cambia el dominio en producci√≥n
      scope: ['email', 'profile'],
    });
  }

  async validate(
    accessToken: string,
    refreshToken: string,
    profile: any,
    done: VerifyCallback,
  ){
    const { name, emails, photos } = profile;
    const user = {
      email: emails[0].value,
      firstName: name.givenName,
      lastName: name.familyName,
      picture: photos[0].value,
      accessToken,
    };
    done(null, user); // Pasa el usuario al contexto
  }
  
}
